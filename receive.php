<?php

//========================================
// RETURN JSON DATA
//========================================

header("Content-Type: application/json");

//========================================
// FILE NAME
//========================================

$file = "messages.json";

//========================================
// CREATE FILE IF NOT EXISTS
//========================================

if(!file_exists($file))
{
    file_put_contents($file, "[]");
}

//========================================
// READ FILE
//========================================

$data = file_get_contents($file);

//========================================
// IF EMPTY
//========================================

if(trim($data) == "")
{
    $data = "[]";
}

//========================================
// RETURN DATA
//========================================

echo $data;

?>